// 1. 'number' + 3 + 3
// Результат: 'number33'
// Крок 1: 'number' + 3 -> рядок + число = рядок 'number3'
// Крок 2: 'number3' + 3 -> знову конкатенація рядків = 'number33'
console.log('number' + 3 + 3);

// 2. null + 3
// Результат: 3
// Пояснення: null при математичних операціях перетворюється на 0. 0 + 3 = 3.
console.log(null + 3);

// 3. 5 && "qwerty"
// Результат: "qwerty"
// Пояснення: Оператор && повертає останнє "істинне" (truthy) значення, якщо обидва істинні. 5 — true, тому результат "qwerty".
console.log(5 && "qwerty");

// 4. +'40' + +'2' + "hillel";
// Результат: "42hillel"
// Крок 1: +'40' перетворює рядок на число 40.
// Крок 2: +'2' перетворює на число 2.
// Крок 3: 40 + 2 = 42.
// Крок 4: 42 + "hillel" (число + рядок) = конкатенація "42hillel".
console.log(+'40' + +'2' + "hillel");

// 5. '10' - 5 === 6;
// Результат: false
// Крок 1: '10' - 5 -> оператор мінус перетворює '10' на число. 10 - 5 = 5.
// Крок 2: 5 === 6 -> суворе порівняння дає false.
console.log('10' - 5 === 6);

// 6. true + false
// Результат: 1
// Пояснення: true стає 1, false стає 0. 1 + 0 = 1.
console.log(true + false);

// 7. '4px' - 3
// Результат: NaN (Not a Number)
// Пояснення: Рядок '4px' містить букви, тому при спробі відняти число він не може стати валідним числом.
console.log('4px' - 3);

// 8. '4' - 3
// Результат: 1
// Пояснення: Мінус змушує рядок '4' стати числом 4. 4 - 3 = 1.
console.log('4' - 3);

// 9. '6' + 3 ** 0;
// Результат: '61'
// Крок 1: Пріоритет у піднесення до степеня. 3 ** 0 = 1.
// Крок 2: '6' + 1 -> конкатенація рядка і числа = '61'.
console.log('6' + 3 ** 0);

// 10. 12 / '6'
// Результат: 2
// Пояснення: Ділення автоматично перетворює рядок '6' на число 6.
console.log(12 / '6');

// 11. '10' + (5 === 6);
// Результат: "10false"
// Крок 1: (5 === 6) дає логічне значення false.
// Крок 2: '10' + false -> додавання до рядка перетворює false на рядок "false".
console.log('10' + (5 === 6));

// 12. null == ''
// Результат: false
// Пояснення: null при нестрогому порівнянні (==) дорівнює лише самому собі або undefined.
console.log(null == '');

// 13. 3 ** (9 / 3);
// Результат: 27
// Крок 1: У дужках 9 / 3 = 3.
// Крок 2: 3 у степені 3 (3 * 3 * 3) = 27.
console.log(3 ** (9 / 3));

// 14. !!'false' == !!'true'
// Результат: true
// Крок 1: !! перетворює будь-який непустий рядок на boolean true.
// Крок 2: true == true -> результат true.
console.log(!!'false' == !!'true');

// 15. 0 || '0' && 1
// Результат: 1
// Крок 1: Пріоритет у &&. '0' — це істинне значення (бо рядок не пустий). '0' && 1 повертає 1.
// Крок 2: 0 || 1 -> оператор || повертає перше істинне значення, тобто 1.
console.log(0 || '0' && 1);

// 16. (+null == false) < 1;
// Результат: false
// Крок 1: +null стає 0.
// Крок 2: 0 == false -> це true (бо false при порівнянні стає 0).
// Крок 3: true < 1 -> true стає 1. 1 < 1 — це false.
console.log((+null == false) < 1);

// 17. false && true || true
// Результат: true
// Крок 1: Пріоритет у &&. false && true = false.
// Крок 2: false || true = true.
console.log(false && true || true);

// 18. false && (false || true);
// Результат: false
// Крок 1: У дужках (false || true) = true.
// Крок 2: false && true = false.
console.log(false && (false || true));

// 19. (+null == false) < 1 ** 5;
// Результат: false
// Крок 1: 1 ** 5 = 1.
// Крок 2: (+null == false) — ми вже вирахували вище, що це true.
// Крок 3: true < 1 -> 1 < 1 = false.
console.log((+null == false) < 1 ** 5)